#include <iostream>
using namespace std;
// Eps 18
// Increment And Decrement Operator

int main() {
    // Preincrement and Postincrement
    int a = 5;
    int b = 5;

    // Postincrement
    cout << a << endl;
    a ++;
    cout << a << endl;
    cout << endl;
    // Preincrement
    cout << b << endl;
    ++b;
    cout << b << endl;

    return 0;
}

