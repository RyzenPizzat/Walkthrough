// Operator Assignment

#include <iostream>
using namespace std;

int main() {

    // Assignment
    int a = 10;
    cout << "Nilai a awalnya adalah : " << a << endl;
    // Operator Menambah ( += )
    a += 3;
    cout << "a ditambah 3 menjadi : " << a << endl;
    // Operator Mengurang ( -= )
    a -= 3;
    cout << "a dikurang 3 menjadi : " << a << endl;
    // Operator Mengkali ( *= )
    a *= 3;
    cout << "a dikali 3 menjadi : " << a << endl;
    // Operator Membagi ( /= )
    a /= 3;
    cout << "a dibagi 3 menjadi : " << a << endl;
    // Operator Modulus ( %= )
    a %= 3;
    cout << "a Modulus 3 menjadi : " << a << endl;
    

}