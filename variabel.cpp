#include <iostream>
using namespace std;

int main() {
    // Integer
    int integer = 0;
    cout << "Integer: " << integer << endl;

    // float
    float floater = 1.5;
    cout << "Float: " << floater << endl;

    // boolean
    bool boolean = true;
    cout << "Boolean: " << boolean << endl;

    // string
    string text = "Hello, World!";
    cout << "String: " << text << endl;
}
